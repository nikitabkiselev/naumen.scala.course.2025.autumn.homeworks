object Task_1 {
  def sumOfDivBy3Or5(iFrom: Int, iTo: Int): Long = {
    // Проверка на некорректный диапазон
    if (iFrom > iTo) {
      return 0L
    }
    // Используем Stream (ленивая коллекция) для эффективности,
    // особенно при больших диапазонах.
    (iFrom to iTo)
      .toStream // Преобразуем в Stream для ленивой оценки
      .filter(num => num % 3 == 0 || num % 5 == 0) // Фильтруем числа, делящиеся на 3 или 5
      .map(_.toLong) // Преобразуем числа в Long перед суммированием, чтобы избежать переполнения
      .sum // Суммируем отфильтрованные числа
  }
}
